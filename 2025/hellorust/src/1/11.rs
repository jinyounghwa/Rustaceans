//Rust의 새도잉(Shadowing)이란?
//Rust에서 새도잉(Shadowing) 은 같은 변수 이름을 다시 선언하여 새로운 값을 할당하는 기법입니다. 
//일반적인 변수 재할당과 다르게, 기존 변수의 타입을 변경할 수도 있으며, mut 키워드 없이도 사용할 수 있습니다.

fn main() {
    let x = 5;
    let x = x + 1; // 새도잉 발생
    let x = x * 2; // 또 다른 새도잉

    println!("x의 값: {}", x); // 12
}
//설명:

//let x = 5; → x는 5로 초기화됩니다.
//let x = x + 1; → 새로운 x가 선언되면서 6이 할당됩니다.
//let x = x * 2; → 또 다른 x가 선언되면서 12가 됩니다.
//최종적으로 x는 12가 출력됩니다.

